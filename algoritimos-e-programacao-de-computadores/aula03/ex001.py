print("Este programa irá calcular a área de um triângulo retângulo.")
base = float(input("Digite o valor em metros da base do triângulo: "))
altura = float(input("Digite o valor em metros da altura do triângulo: "))
area = (base * altura) /2
print(f"A área do triângulo retângulo é {area}m²")
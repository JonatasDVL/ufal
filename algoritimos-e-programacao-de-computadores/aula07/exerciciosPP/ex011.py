# Exercício Pré-Prova 011

# Claro! Aqui estão 5 exercícios mais desafiadores para cada um dos tópicos anteriores:

# Listas:

# Crie uma lista de números aleatórios e encontre o segundo menor e o segundo maior número na lista.

# Dada uma lista de palavras, crie uma função que retorne a palavra mais longa da lista.

# Implemente uma função que remova elementos duplicados de uma lista sem usar conjuntos (sets).

# Crie uma lista de números e uma função que retorne a soma dos elementos em posições ímpares da lista.

# Crie uma lista de strings e ordene-a com base no comprimento das strings, da menor para a maior.

# Tuplas:

# Crie uma tupla de coordenadas (x, y) que representam pontos no plano cartesiano. Ordene a tupla com base na distância do ponto à origem.

# Dada uma lista de tuplas, onde cada tupla contém o nome de um aluno e suas notas em três testes, calcule a média de cada aluno e ordene a lista com base nas médias.

# Crie uma função que receba uma tupla de números inteiros e retorne uma nova tupla contendo apenas os números primos da tupla original.

# Implemente uma função que receba duas tuplas de números inteiros e retorne uma tupla contendo os elementos que aparecem em ambas as tuplas.

# Crie uma tupla com informações de produtos (nome, preço, quantidade em estoque). Implemente uma função que calcule o valor total do estoque.

# Dicionários:

# Crie um dicionário de palavras como chaves e suas frequências em um texto como valores. Encontre a palavra mais comum no texto.

# Crie um dicionário de contatos com nome e número de telefone. Implemente uma função que permita a busca por nome e número.

# Crie um dicionário de notas de alunos com matrículas como chaves e notas como valores. Encontre a matrícula do aluno com a maior média.

# Implemente uma função que receba uma lista de dicionários representando produtos (com nome, preço e quantidade) e retorne o produto mais caro.

# Crie um dicionário com informações de funcionários (nome, salário, departamento). Calcule a média salarial por departamento.

# Conjuntos (Sets):

# Crie dois conjuntos com números primos e implemente uma função que retorne a união dos conjuntos sem duplicatas.

# Crie um conjunto de letras do alfabeto e verifique se ele é um subconjunto de outra lista de letras.

# Implemente uma função que encontre a diferença simétrica entre dois conjuntos.

# Crie dois conjuntos com números e encontre o conjunto de todos os números que estão em um dos conjuntos, mas não em ambos.

# Crie um conjunto de palavras e implemente uma função que retorne todas as palavras que começam com uma letra específica.

# Matrizes:

# Crie duas matrizes 3x3 e implemente uma função que realize a multiplicação de matrizes entre elas.

# Implemente uma função que calcule a transposta de uma matriz (troque linhas por colunas).

# Crie uma função que verifique se uma matriz é simétrica (igual à sua transposta).

# Implemente uma função que encontre o determinante de uma matriz quadrada.

# Crie uma matriz 4x4 e implemente uma função que encontre a matriz inversa, se existir.

# Estes exercícios são mais desafiadores e abordam conceitos mais avançados em Python e manipulação de estruturas de dados. Eles são ideais para aprimorar suas habilidades em programação.